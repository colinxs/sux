// Generate sux/src/skill-prompt.ts — the sux + life SKILLs embedded as strings,
// so the /mcp server can serve them as MCP prompts to every client (mobile
// included) that can't load the local plugin skills. Sources of truth are
// .claude/skills/{sux,life}/SKILL.md. Run: `npm run gen:skill`.
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..", "..");
const OUT = join(ROOT, "sux", "src", "skill-prompt.ts");
const SKILLS = ["sux", "life"];

// Split the YAML frontmatter (---\n…\n---) from the markdown body. The body is the
// prompt text; the frontmatter `description` becomes the prompt's description.
function extract(name) {
	const raw = readFileSync(join(ROOT, ".claude", "skills", name, "SKILL.md"), "utf8");
	const m = raw.match(/^---\n([\s\S]*?)\n---\n?([\s\S]*)$/);
	const front = m ? m[1] : "";
	const body = (m ? m[2] : raw).trim();
	const description = (front.match(/^description:\s*(.*)$/m)?.[1] ?? `${name} skill guidance`).trim();
	return { body, description };
}

const [sux, life] = SKILLS.map(extract);
const out = `// GENERATED by scripts/gen-skill-prompt.mjs (npm run gen:skill) from
// .claude/skills/{sux,life}/SKILL.md. Do not edit — edit the SKILLs and regenerate.
// Embedded because a Cloudflare Worker has no runtime filesystem; the /mcp server
// serves these as MCP prompts (prompts/get) so every client gets the guidance even
// without the local plugin skills.

export const SUX_SKILL_DESCRIPTION = ${JSON.stringify(sux.description)};

export const SUX_SKILL_PROMPT = ${JSON.stringify(sux.body)};

export const LIFE_SKILL_DESCRIPTION = ${JSON.stringify(life.description)};

export const LIFE_SKILL_PROMPT = ${JSON.stringify(life.body)};
`;
writeFileSync(OUT, out);
console.log(`Wrote sux/src/skill-prompt.ts — sux ${sux.body.length} + life ${life.body.length} chars.`);
